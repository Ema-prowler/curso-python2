# Conversion de tipos de datos


x = 1
y = 2.0
z = "3"

print(x)    # int
print(y)    # float
print(z)    # str
print("separador")
# Para convertir un numero a una cadena o un entero o un float
# Utilizamos la funcion str()
# Tambien podemos utilizar la funcion int() o float() para sus respectivos tipos.


print(str(y))
print(int(y))
print("separador")

# para asignar y convertir un tipo de datos a otro:

x = float(x)
print(x)

y = int(y)
print(y)

